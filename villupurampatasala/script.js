    const root = document.documentElement;
    const langToggle = document.getElementById("langToggle");
    const langLabel = document.getElementById("langLabel");
    const navLinks = document.getElementById("navLinks");
    const menuToggle = document.getElementById("menuToggle");
    const musicToggle = document.getElementById("musicToggle");
    const devotionalAudio = document.getElementById("devotionalAudio");
    const admissionForm = document.getElementById("admissionForm");
    const scrollTopButton = document.getElementById("scrollTop");
    const lightbox = document.getElementById("lightbox");
    const lightboxImage = document.getElementById("lightboxImage");
    const lightboxClose = document.getElementById("lightboxClose");
    const animatedElements = document.querySelectorAll(".fade-up");
    const galleryItems = document.querySelectorAll(".gallery-item");

    let currentLanguage = "en";

    function updateMusicButton() {
      const key = devotionalAudio.paused ? "musicToggle" : "musicPlaying";
      musicToggle.textContent = translations[currentLanguage][key];
    }

    function applyTranslations(language) {
      document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.dataset.i18n;
        if (translations[language][key]) {
          element.textContent = translations[language][key];
        }
      });

      const nameInput = document.getElementById("name");
      const ageInput = document.getElementById("age");
      const contactInput = document.getElementById("contactNumber");
      const courseSelect = document.getElementById("course");

      if (language === "ta") {
        document.title = "விழுப்புரம் சங்கர மடம் – ஸ்ரீ மஹா பெரியவாவின் அவதார ஸ்தலம்";
        root.lang = "ta";
        langLabel.textContent = "தமிழ்";
        nameInput.placeholder = "முழுப் பெயரை உள்ளிடவும்";
        ageInput.placeholder = "வயதை உள்ளிடவும்";
        contactInput.placeholder = "பெற்றோர் கைபேசி எண்";
        courseSelect.options[0].textContent = translations.ta.formSelectCourse;
      } else {
        document.title = "Villupuram Sankara Madam – Shri Maha Periyava's Birth Place";
        root.lang = "en";
        langLabel.textContent = "English";
        nameInput.placeholder = "Enter full name";
        ageInput.placeholder = "Enter age";
        contactInput.placeholder = "Parent mobile number";
        courseSelect.options[0].textContent = translations.en.formSelectCourse;
      }

      updateMusicButton();
    }

    langToggle.addEventListener("click", () => {
      currentLanguage = currentLanguage === "en" ? "ta" : "en";
      applyTranslations(currentLanguage);
    });

    menuToggle.addEventListener("click", () => {
      navLinks.classList.toggle("open");
    });

    document.querySelectorAll(".nav-links a").forEach((link) => {
      link.addEventListener("click", () => navLinks.classList.remove("open"));
    });

    musicToggle.addEventListener("click", () => {
      if (devotionalAudio.paused) {
        devotionalAudio.play().catch(() => {
          const message = currentLanguage === "ta"
            ? "ஆடியோ இயங்கவில்லை. கோப்பு பாதையை சரிபார்க்கவும்."
            : "Audio could not be played. Please verify the file path.";
          alert(message);
        });
      } else {
        devotionalAudio.pause();
      }
      updateMusicButton();
    });

    devotionalAudio.addEventListener("play", updateMusicButton);
    devotionalAudio.addEventListener("pause", updateMusicButton);
    devotionalAudio.addEventListener("ended", updateMusicButton);

    admissionForm.addEventListener("submit", (event) => {
      event.preventDefault();
      const message = currentLanguage === "ta"
        ? "உங்கள் சேர்க்கை விண்ணப்பம் பெறப்பட்டது. இது தற்போது மாதிரி படிவம்."
        : "Your admission request has been received. This is currently a placeholder form.";
      alert(message);
      admissionForm.reset();
    });

    const observer = new IntersectionObserver((entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("visible");
          observer.unobserve(entry.target);
        }
      });
    }, { threshold: 0.15 });

    animatedElements.forEach((element) => observer.observe(element));

    window.addEventListener("scroll", () => {
      if (window.scrollY > 400) {
        scrollTopButton.classList.add("visible");
      } else {
        scrollTopButton.classList.remove("visible");
      }
    });

    scrollTopButton.addEventListener("click", () => {
      window.scrollTo({ top: 0, behavior: "smooth" });
    });

    galleryItems.forEach((item) => {
      item.addEventListener("click", () => {
        lightboxImage.src = item.dataset.image;
        lightbox.classList.add("open");
        lightbox.setAttribute("aria-hidden", "false");
      });
    });

    function closeLightbox() {
      lightbox.classList.remove("open");
      lightbox.setAttribute("aria-hidden", "true");
      lightboxImage.src = "";
    }

    lightboxClose.addEventListener("click", closeLightbox);
    lightbox.addEventListener("click", (event) => {
      if (event.target === lightbox) closeLightbox();
    });

    window.addEventListener("keydown", (event) => {
      if (event.key === "Escape") closeLightbox();
    });

    applyTranslations(currentLanguage);
